let currentLang = 'en';

// --- Functions ---

function setLanguage(lang) {
    currentLang = lang;
    
    // Update HTML dir and lang
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    // Update Toggle Button Text
    const langText = document.getElementById('lang-text');
    if(langText) {
        langText.innerText = translations[lang]['lang_btn'];
    }

    // Update all translatable elements
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    });
}

function toggleLanguage() {
    const newLang = currentLang === 'en' ? 'ar' : 'en';
    setLanguage(newLang);
}

// --- Initialization & Event Listeners ---

// Remove Preloader after exactly 2 seconds
setTimeout(() => {
    const preloader = document.getElementById('preloader');
    if (preloader) {
        preloader.classList.add('hidden');
        // Remove from DOM after transition completes
        setTimeout(() => preloader.remove(), 600);
    }
}, 2000);

document.addEventListener('DOMContentLoaded', () => {
    // 1. Language Toggle Setup
    const langBtn = document.getElementById('lang-toggle');
    if(langBtn) {
        langBtn.addEventListener('click', toggleLanguage);
    }
    
    // 2. Mobile Menu Toggle
    const mobileBtn = document.getElementById('mobile-menu-btn');
    const mobileMenu = document.getElementById('mobile-menu');
    
    if(mobileBtn && mobileMenu) {
        mobileBtn.addEventListener('click', () => {
            mobileMenu.classList.toggle('hidden');
        });
        
        // Close menu on link click
        mobileMenu.querySelectorAll('a').forEach(link => {
            link.addEventListener('click', () => {
                mobileMenu.classList.add('hidden');
            });
        });
    }

    // 3. Navbar Scroll Effect
    const navbar = document.getElementById('navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('py-2');
            navbar.classList.remove('py-4');
        } else {
            navbar.classList.add('py-4');
            navbar.classList.remove('py-2');
        }
    });

    // 4. Scroll Reveal Animations setup using Intersection Observer
    const revealElements = document.querySelectorAll('.reveal');
    
    const revealOptions = {
        threshold: 0.15,
        rootMargin: "0px 0px -50px 0px"
    };

    const revealOnScroll = new IntersectionObserver(function(entries, observer) {
        entries.forEach(entry => {
            if (!entry.isIntersecting) {
                return;
            } else {
                entry.target.classList.add('active');
                // Optional: Stop observing once revealed
                observer.unobserve(entry.target);
            }
        });
    }, revealOptions);

    revealElements.forEach(el => {
        revealOnScroll.observe(el);
    });

    // 5. WebMCP Implementation
    if (navigator.modelContext && navigator.modelContext.provideContext) {
        navigator.modelContext.provideContext({
            tools: [
                {
                    name: "getAcademyInfo",
                    description: "Get information about Nexus Academy services and programs",
                    inputSchema: {
                        type: "object",
                        properties: {}
                    },
                    execute: async (args) => {
                        return { 
                            result: "Nexus Academy is an educational institution providing Offline Courses, Online Courses, an Academy Platform, Internships, and specialized programs like Nexus Hub, P-Smart, and Simulation." 
                        };
                    }
                }
            ]
        });
    }
});
